Fantasma::Fantasma(Vector2f position){
    if (!texture.loadFromFile("fantasmaP.png"))
    {
        std::cout<<"error"<<endl;
    }
    texture.setRepeated(true);
    this->fantasma.setTexture(texture);
    this->fantasma.setPosition(position);
    
}

